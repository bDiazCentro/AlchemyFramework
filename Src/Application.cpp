void key_callback(GLFWwindow* window, int key, int scancode, int action, int mods)
{
	switch (action)
	{
	case GLFW_REPEAT:
		Input::holdkey = key;
		Input::holdState = true;
		break;
	case GLFW_PRESS:
		Input::presskey = key;
		Input::pressState = true;

		break;
	case GLFW_RELEASE:
		Input::releaseKey = key;
		Input::releaseState = true;
		break;
	}
}

void mouse_callback(GLFWwindow* window, int button, int action, int mods)
{
	switch (action)
	{
	case GLFW_PRESS:
		Input::pressButton = button;
		Input::pressStateButton = true;
		break;
	case GLFW_RELEASE:
		Input::releaseButton = button;
		Input::releaseStateButton = true;
		break;
	}
}

void mouse_position_callback(GLFWwindow* window, double xpos, double ypos)
{
	Input::mousePosition = Vector2(xpos, ypos);
}

void Application::MainLoop()
{
	while (!glfwWindowShouldClose(window)) {
		Time::GetInitValue();
		glfwPollEvents();
		glfwSetKeyCallback(window, key_callback);
		glfwSetMouseButtonCallback(window, mouse_callback);
		glfwSetCursorPosCallback(window, mouse_position_callback);
		UpdateUniformBuffer();
		DrawFrame();
		Time::Elapsed();
		timer += Time::deltatime;
		if (timer > 0.2f) {
			++image_counter; 
			timer = 0;
		}
		if (image_counter > 3)image_counter = 0;
	}
	vkDeviceWaitIdle(device);
}


void Application::OnWindowResized(GLFWwindow* window, int width, int height)
{
	if (width == 0 || height == 0) return;

	Application* app = reinterpret_cast<Application*>(glfwGetWindowUserPointer(window));
	app->RecreateSwapChain();
}

