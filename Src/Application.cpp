void Application::UpdateActualCameraPosition()
{
	float CameraSpeed = 0.001f;
	float lastX = CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform.Position().X();
	float lastY = CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform.Position().Y();
	float lastZ = CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform.Position().Z();
	float actualX = lastX;
	float actualY = lastY;
	float actualZ = lastZ;
	if (left)
	{
		actualX -= CameraSpeed;
		CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform.Position(Vector3(actualX, lastY, lastZ));
	}
	if (right)
	{
		actualX += CameraSpeed;
		CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform.Position(Vector3(actualX, lastY, lastZ));
	}
	if (up)
	{
		actualZ += CameraSpeed;
		CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform.Position(Vector3(lastX, lastY, actualZ));
	}
	if (down)
	{
		actualZ -= CameraSpeed;
		CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform.Position(Vector3(lastX, lastY, actualZ));
	}
}

