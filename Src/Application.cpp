void Application::UpdateUniformBuffer()
{
	static auto startTime = std::chrono::high_resolution_clock::now();

	auto currentTime = std::chrono::high_resolution_clock::now();
	float time = std::chrono::duration_cast<std::chrono::milliseconds>(currentTime - startTime).count() / 10000.0f;

	for (auto object : GameManager::Instance()->gameObjects)
	{
		if (object->objectHasSpriteComponent)
		{
			if (!object->GetComponent(ComponentType::SPRITE)->spriteRenderer.enabled)continue;
			object->ubo.model = glm::translate(glm::mat4(1.0f),
				glm::vec3(object->GetComponent(ComponentType::TRANSFORM)->transform.Position().X(), 
					object->GetComponent(ComponentType::TRANSFORM)->transform.Position().Y(), 
					object->GetComponent(ComponentType::TRANSFORM)->transform.Position().Z()));
			CameraComponent *cc = CameraManager::Instance()->GetActiveCamera();
			TransformComponent *tc = &CameraManager::Instance()->GetActiveCamera()->Father().GetComponent(ComponentType::TRANSFORM)->transform;
			object->ubo.view = glm::lookAt(glm::vec3(tc->Position().X(), tc->Position().Y(), tc->Position().Z()),
				glm::vec3(0.0f, 0.0f, 0.0f), glm::vec3(0.0f, 0.0f, 1.0f));
			object->ubo.proj = glm::perspective(glm::radians(cc->fov),
				swapChainExtent.width / (float)swapChainExtent.height, cc->near, cc->far);
			object->ubo.proj[1][1] *= -1;

			void* this_data;
			vkMapMemory(device, object->uniformBufferMemory, 0, sizeof(object->ubo), 0, &this_data);
			memcpy(this_data, &object->ubo, sizeof(object->ubo));
			vkUnmapMemory(device, object->uniformBufferMemory);
		}
	}

	Update();
}

void Application::DrawFrame()
{
	uint32_t imageIndex;
	VkResult result = vkAcquireNextImageKHR(device, swapChain, std::numeric_limits<uint64_t>::max(), 
		imageAvailableSemaphore, VK_NULL_HANDLE, &imageIndex);

	if (result == VK_ERROR_OUT_OF_DATE_KHR) {
		RecreateSwapChain();
		return;
	}
	else if (result != VK_SUCCESS && result != VK_SUBOPTIMAL_KHR) {
		throw std::runtime_error("failed to acquire swap chain image!");
	}

	VkSubmitInfo submitInfo = {};
	submitInfo.sType = VK_STRUCTURE_TYPE_SUBMIT_INFO;

	VkSemaphore waitSemaphores[] = { imageAvailableSemaphore };
	VkPipelineStageFlags waitStages[] = { VK_PIPELINE_STAGE_COLOR_ATTACHMENT_OUTPUT_BIT };
	submitInfo.waitSemaphoreCount = 1;
	submitInfo.pWaitSemaphores = waitSemaphores;
	submitInfo.pWaitDstStageMask = waitStages;

	submitInfo.commandBufferCount = 1;
	submitInfo.pCommandBuffers = &commandBuffers[imageIndex];

	VkSemaphore signalSemaphores[] = { renderFinishedSemaphore };
	submitInfo.signalSemaphoreCount = 1;
	submitInfo.pSignalSemaphores = signalSemaphores;

	if (vkQueueSubmit(graphicsQueue, 1, &submitInfo, VK_NULL_HANDLE) != VK_SUCCESS) {
		throw std::runtime_error("failed to submit draw command buffer!");
	}

	VkPresentInfoKHR presentInfo = {};
	presentInfo.sType = VK_STRUCTURE_TYPE_PRESENT_INFO_KHR;

	presentInfo.waitSemaphoreCount = 1;
	presentInfo.pWaitSemaphores = signalSemaphores;

	VkSwapchainKHR swapChains[] = { swapChain };
	presentInfo.swapchainCount = 1;
	presentInfo.pSwapchains = swapChains;

	presentInfo.pImageIndices = &imageIndex;

	result = vkQueuePresentKHR(presentQueue, &presentInfo);

	if (result == VK_ERROR_OUT_OF_DATE_KHR || result == VK_SUBOPTIMAL_KHR) {
		RecreateSwapChain();
	}
	else if (result != VK_SUCCESS) {
		throw std::runtime_error("failed to present swap chain image!");
	}
	vkQueueWaitIdle(presentQueue);

}

