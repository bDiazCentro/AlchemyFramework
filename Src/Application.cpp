void Application::InitVulkan()
{
	CreateInstance();
	SetupDebugCallback();
	CreateSurface();
	PickPhysicalDevice();
	CreateLogicalDevice();
	CreateSwapChain();
	CreateImageViews();
	CreateRenderPass();
	CreateDescriptorSetLayout();
	CreateGraphicsPipeline();
	CreateCommandPool();
	CreateDepthResources();
	CreateFramebuffers();
	AssetsManager::Instance()->DebugAssetsManager();

	for (int i = 0; i < num_images_png; ++i)
	{
		std::stringstream path;
		path << "textures/" << images_png[i] << ".png";
		const std::string tmp = path.str();
		const char* finalPath = tmp.c_str();
		AssetsManager::Instance()->LoadImage(images_png[i], LoadImageToAssetsManager(finalPath));
	}

	for (int i = 0; i < num_images_jpg; ++i)
	{
		std::stringstream path;
		path << "textures/" << images_jpg[i] << ".jpg";
		const std::string tmp = path.str();
		const char* finalPath = tmp.c_str();
		AssetsManager::Instance()->LoadImage(images_jpg[i], LoadImageToAssetsManager(finalPath));
	}

	AssetsManager::Instance()->DebugAssetsManager();
	CreateVertexBuffer();
	CreateIndexBuffer();

	for (auto obj : GameManager::Instance()->gameObjects)
	{
		if (!obj->objectHasSpriteComponent)continue;
		if (!obj->GetComponent(ComponentType::SPRITE)->spriteRenderer.enabled)continue;
		CreateUniformBuffer(obj);
	}
	CreateDescriptorPool();

	for (auto obj : GameManager::Instance()->gameObjects)
	{
		if (!obj->objectHasSpriteComponent)continue;
		if (!obj->GetComponent(ComponentType::SPRITE)->spriteRenderer.enabled)continue;
		CreateDescriptorSet(obj);
	}

	CreateCommandBuffers();
	CreateSemaphores();
}

